//CARD FUNCIONAL MOBILE
var boxfuncionalmobile = document.getElementById("boxfuncionalmobile");
var funcionalmobile = document.getElementById("funcionalmobile");
var aboutfuncionalmobile = document.getElementById("aboutfuncionalmobile");

boxfuncionalmobile.addEventListener('click',function(){
        if(funcionalmobile.classList.contains('close')){ 
        funcionalmobile.classList.remove('close');
        aboutfuncionalmobile.classList.add('close');
         }
         else{
            funcionalmobile.classList.add('close');
            aboutfuncionalmobile.classList.remove('close');
         }
})

