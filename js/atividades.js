var boxfuncional = document.getElementById("boxfuncional");
var funcional = document.getElementById("funcional");
var aboutfuncional = document.getElementById("aboutfuncional");



boxfuncional.addEventListener('mouseout',function(){
  
        funcional.classList.remove('close');
        aboutfuncional.classList.add('close');
       
   
})

boxfuncional.addEventListener('mouseover',function(){
    funcional.classList.add('close');
        aboutfuncional.classList.remove('close');
})


